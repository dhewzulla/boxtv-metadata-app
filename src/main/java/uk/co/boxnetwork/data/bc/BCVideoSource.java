package uk.co.boxnetwork.data.bc;

public class BCVideoSource {
   private String asset_id;
   private Boolean remote;
   private String src;
   private String codec;
   private String container;
   private Long encoding_rate;
   private Long duration;
   private Integer height;
   private Integer width;
   private Long size;
   private String uploaded_at;
   private String type;
public String getAsset_id() {
	return asset_id;
}
public void setAsset_id(String asset_id) {
	this.asset_id = asset_id;
}
public Boolean getRemote() {
	return remote;
}
public void setRemote(Boolean remote) {
	this.remote = remote;
}
public String getSrc() {
	return src;
}
public void setSrc(String src) {
	this.src = src;
}
public String getCodec() {
	return codec;
}
public void setCodec(String codec) {
	this.codec = codec;
}
public String getContainer() {
	return container;
}
public void setContainer(String container) {
	this.container = container;
}
public Long getEncoding_rate() {
	return encoding_rate;
}
public void setEncoding_rate(Long encoding_rate) {
	this.encoding_rate = encoding_rate;
}
public Long getDuration() {
	return duration;
}
public void setDuration(Long duration) {
	this.duration = duration;
}
public Integer getHeight() {
	return height;
}
public void setHeight(Integer height) {
	this.height = height;
}
public Integer getWidth() {
	return width;
}
public void setWidth(Integer width) {
	this.width = width;
}
public Long getSize() {
	return size;
}
public void setSize(Long size) {
	this.size = size;
}
public String getUploaded_at() {
	return uploaded_at;
}
public void setUploaded_at(String uploaded_at) {
	this.uploaded_at = uploaded_at;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
   
	
}
